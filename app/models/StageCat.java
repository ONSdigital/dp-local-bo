package models;

public class StageCat {
	
	private String dimId;
	private String dimLabel;
	private String dimItemId;
	private String dimItemLabel;
	
	public String getDimId() {
		return dimId;
	}
	public void setDimId(String dimId) {
		this.dimId = dimId;
	}
	public String getDimLabel() {
		return dimLabel;
	}
	public void setDimLabel(String dimLabel) {
		this.dimLabel = dimLabel;
	}
	public String getDimItemId() {
		return dimItemId;
	}
	public void setDimItemId(String dimItemId) {
		this.dimItemId = dimItemId;
	}
	public String getDimItemLabel() {
		return dimItemLabel;
	}
	public void setDimItemLabel(String dimItemLabel) {
		this.dimItemLabel = dimItemLabel;
	}
	
}
